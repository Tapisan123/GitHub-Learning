# Write a Python program to create a list of numbers and print the average of the numbers

# List of numbers
numbers = [10, 20, 30, 40, 50]

# Calculate sum of all numbers
sum = 0
for number in numbers:
    sum += number

# Calculate average
average = sum / len(numbers)
print("Average of the numbers:", average)
